package Function_calls;

public class calci {
	public void findAdd(int num1,int num2) {
		System.out.println("ADDITION:"+(num1+num2));
	}
	public void findSub(int num1,int num2) {
		System.out.println("SUBRACTION:"+(num1-num2));
	}
	public void findMul(int num1,int num2) {
		System.out.println("MULTIPLY:"+(num1*num2));
	}
	public void findDivi(int num1,int num2) {
		System.out.println("Division:"+(num1/num2));
	}
	public void findModi(int num1,int num2) {
		System.out.println("MODULUS:"+(num1%num2));
		
	}

}
